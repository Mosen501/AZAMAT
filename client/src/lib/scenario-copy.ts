export function localizeScenarioSummary(
  summary: ScenarioSummary,
  isArabic: boolean,
): ScenarioSummary {
  if (!isArabic) {
    return summary;
  }

  const copy = SCENARIO_COPY_AR[summary.id];

  if (!copy) {
    return summary;
  }

  return {
    ...summary,
    title: copy.title,
    description: copy.description,
  };
}

export function localizeScenario(scenario: Scenario, isArabic: boolean): Scenario {
  if (!isArabic) {
    if (scenario.translations?.en) {
      return applyLocalizedContent(scenario, scenario.translations.en);
    }

    return scenario;
  }

  if (scenario.translations?.ar) {
    return applyLocalizedContent(scenario, scenario.translations.ar);
  }

  const copy = SCENARIO_COPY_AR[scenario.id];

  if (!copy) {
    return scenario;
  }

  return applyLocalizedContent(scenario, toLocalizedContent(copy));
}
