import type { LocalizedScenarioContent, Scenario, ScenarioSummary } from "@shared/schema";

type ScenarioArabicCopy = {
  title: string;
  description: string;
  steps: Record<
    string,
    {
      description: string;
      choices: Record<string, string>;
    }
  >;
};

function toLocalizedContent(copy: ScenarioArabicCopy): LocalizedScenarioContent {
  return {
    title: copy.title,
    description: copy.description,
    steps: Object.fromEntries(
      Object.entries(copy.steps).map(([stepId, stepCopy]) => [
        stepId,
        {
          description: stepCopy.description,
          choices: stepCopy.choices,
        },
      ]),
    ),
  };
}

function applyLocalizedContent(
  scenario: Scenario,
  content: LocalizedScenarioContent,
): Scenario {
  return {
    ...scenario,
    title: content.title,
    description: content.description,
    steps: Object.fromEntries(
      Object.entries(scenario.steps).map(([stepId, step]) => {
        const stepContent = content.steps[stepId];

        if (!stepContent) {
          return [stepId, step];
        }

        return [
          stepId,
          {
            ...step,
            description: stepContent.description,
            choices: step.choices.map((choice) => ({
              ...choice,
              text: stepContent.choices[choice.id] ?? choice.text,
            })),
          },
        ];
      }),
    ),
  };
}

